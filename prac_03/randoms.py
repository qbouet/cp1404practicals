import random

print(random.randint(5, 20))  # line 1
# What did you see on line 1?
# What was the smallest number you could have seen, what was the largest?
# I saw the number 11 on line 1.
# The smallest number I could have seen is 5 and the largest is 20

print(random.randrange(3, 10, 2))  # line 2
# What did you see on line 2?
# What was the smallest number you could have seen, what was the largest?
# Could line 2 have produced a 4?
# I saw the number 7 on line 2.
# The smallest number I could have seen is 3 and the largest is 9.
# Line 2 can never produce a 4 as it is in steps of 2 and starts at 3.

print(random.uniform(2.5, 5.5))  # line 3
